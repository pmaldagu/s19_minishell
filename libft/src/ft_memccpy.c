#include "../include/libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t	i;
	char	*s;
	char	*d;

	i = 0;
	s = (char*)src;
	d = (char*)dst;
	while (i < n)
	{
		d[i] = s[i];
		if (s[i] == (char)c)
			return (&d[i + 1]);
		i++;
	}
	return (NULL);
}
